CREATE DATABASE BINGO;

USE BINGO;

CREATE TABLE USER(
id_player int NOT NULL auto_increment,
PRIMARY KEY(id_player)
)ENGINE=INNODB;


CREATE TABLE GAME(
id_game INT AUTO_INCREMENT,
PRIMARY KEY(id_game)
)ENGINE=INNODB;


CREATE TABLE BALL(
id_ball INT AUTO_INCREMENT,
PRIMARY KEY(id_ball)
)ENGINE=INNODB;


